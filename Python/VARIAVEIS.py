''' 
    Tipos de Variaveis

    int: numeros inteiros, ou seja, números sem parte decimal.
    float: numeros reais, ou seja, numeros decimais. 
    str: cadeias de caracteres, ou seja, dados textuais.
    bool: valores lógicos(booleanos): True ou False (T e F maiúsculo)
    
'''

# criando variaveis e inserindo valores (int, float, str, bool):

